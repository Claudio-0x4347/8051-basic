	WR_COMANDO	EQU 0000H
	WR_DADOS	EQU 0001H
	RD_COMANDO	EQU 0002H
	RD_DADOS	EQU 0003H

	ORG 00H
	LJMP INICIO
	
	ORG 30H
INICIO:	MOV SP,#2FH   
	MOV R0,#00
	MOV R1,#00
	MOV R7,#00H
	LCALL INICIA
	LCALL LIMPA

LINHA1:	MOV A,R7
	MOV DPTR,#MSG1
	MOVC A,@A+DPTR
	CJNE A,#0FEH,V1

	; muda para a linha 2
	MOV A, #0C0H
	LCALL INSTR_WR
	MOV R7, #00H
LINHA2: MOV A,R7
	MOV DPTR,#MSG2
	MOVC A,@A+DPTR
	CJNE A,#09H,V2
	SJMP $
	
V1:	LCALL TEXTO_WR
	LCALL ATRASO_LCD
	INC R7
	SJMP LINHA1
	
V2:	LCALL TEXTO_WR
	LCALL ATRASO_LCD
	INC R7
	SJMP LINHA2

	; SUBROTINA DE INICIALIZAÇÃO DO DISPLAY LCD
INICIA:	MOV A,#38H
	LCALL INSTR_WR
	
	MOV A,#38H
	LCALL INSTR_WR
	
	MOV A,#0EH
	LCALL INSTR_WR
	
	MOV A,#06H
	LCALL INSTR_WR
	RET
	
	; SUBROTINA QUE LIMPA O DISPLAY
LIMPA:	MOV A,#01H
	LCALL INSTR_WR
	RET

INSTR_WR:
	MOV DPTR,#WR_COMANDO	; end. 0000H
	MOVX @DPTR,A
	LCALL ATRASO_LCD
	RET
	
TEXTO_WR:
	MOV DPTR,#WR_DADOS	; end. 0001H
	MOVX @DPTR,A
	LCALL ATRASO_LCD
	RET

ATRASO_LCD:
	MOV R4,#10
V6:	MOV R5,#80
	DJNZ R5,$
	DJNZ R4,V6
	RET
	
MSG1:	DB 'CLAUDIO GABRIEL', 0FEH
MSG2:	DB '201618309', 09H
FIM:	NOP
	END
